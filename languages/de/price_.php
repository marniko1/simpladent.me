<div class="desc">
<p>Der genaue Preis ist bedingt durch die verwendeten Implantat-Typen und patientenspezifische Bedürfnisse.  
Drei Beispiele verdeutlichen die unterschiedlichen Versorgungsweisen: <br />
1. Ein Frontzahn oder ein Prämolar werden durch ein Strategic Implant ersetzt. Die Kosten der Implantation betragen rund 500,- Euro.<br />
2. Ein unterer 1. Molar wird durch zwei Strategic Implant ersetzt. Die Kosten betragen rund 750,- Euro.<br />
3. Ein unterer oder oberer 2. Molar, oder ein oberer 1. Molar werden in Sofortbelastung durch drei Strategic Implant ersetzt. Die Kosten betragen ca 950,-- Euro.<br />
Hinzu kommen die Kosten für die protethetische Versorgung.</p>
<p>Der Preis für die Brücke hängt auch von der Herstellungsweise und dem von Ihnen gewählten Material ab.</p>
<p>Dauer: 2 - 5 Tage</p>
<p>&nbsp;</p>
<p>*Der Preis ist vom Aufwand und der Schwierigkeit des Falles abhängig und kann sogar noch niedriger sein.  Ferner hängt der Preis von der Materialwahl für die Brücken ab (Metal-Acryl, Metall-Composite, Metall-Keramik, Zirkon) </p>
<p>*Der endgültige Preis für die Behandlung wird nach der ersten Diagnose und dem Vorliegen der Röntgenunterlagen sowie des medizinischen Fragebogens festgelegt. Es bestehen Preisunterschiede zwischen einzelnen Kliniken. Diese Unterschiede hängen mit der Lage und der Ausstattung der Kliniken sowie vom Gesamtangebot (d.h. nicht nur von der Behandlung) ab.</p>
<p>*Die Behandlungen werden in Euro offeriert, die Bezahlung erfolgt in Euro oder der örtlichen Landeswährung (Preis abhängig vom aktuellen Wechselkurs zum Behandlungszeitpunkt).</p>
