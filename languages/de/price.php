                    <h1>Behandlungdmöglichkeiten und Kosten</h1>
                    <div class="article">
                        <p>Mit unserer Behandlungsmethode sparen Sie eine Menge Geld. Dies wird nicht durch billige Implantate oder Bauteile ermöglicht, 
						sondern durch eine moderne und effektive Berhandlungstechnologie. Im Vergleich zu herkömmlichen Implantat-Behandlungen sparen Sie bis zu zwei Dritteln der 
						Gesamtkosten, da Sie weniger Behandlungssitzungen, keinen Knochenaufbau und kaum Medikamente benötigen. Der Gesamtpreis des Behandlung kann nach dem Vorliegen von Röntgenbildern und der medizinischen 
						Dokumentation bestimmt werden. Selbstverständlich werden Ihre individuellen Behandlungswünsche berücksichtigt  </p>
                    </div>
