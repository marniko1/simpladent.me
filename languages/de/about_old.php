        <h1>Geschichte</h1>
        <div class="article">
            <p>Die <strong>Dr. IhdeDental</strong> Gruppe wurde 1954  vom Zahntechniker Klaus Ihde in Berlin gegründet. Die Firma zeichnete sich von Anfang an als Lieferant von qualitativ hochwertigen Dentalmaterialen aus. 
			Seit Ende der 80er Jahre wandelte sich das Unternehmen langsam in ein Herstellungsunternehmen um.</p>
            <p>Dr. IhdeDental ist heute einer der führenden Herstellern von Dentalimplantaten für die Sofortbelastung. Die Produkte werden in über 40 Ländern verwendet. 
			Mehr als 1.000.000 Implantate wurden bisher in den Produktinosstätten in der Schweiz produziert und anschliessend weltweit eingesetzt.</p>
            <p>Dr. IhdeDental  hat zusammen mit <strong>Simpladent</strong> Implantate und ein Behandlungsverfahren entwickelt, welches selbst in schwierigen Fällen hervorragende Ergebnisse bringt. Es wird nur sehr 
			wenig Knochen benötigt.  Damit wurden Knochenaufbauten und Knochentransplantationen komplett unnötig.</p>
            <p>Den wissenschaftlichen Teil der Unternehmensgruppe führt Dr. Stefan Ihde geführt, der in Deutschland als Zahnarzt ausgebildet wurde und später Weiterbildungen zum Parodontologen und Oralchirurgen absolvierte.  
			Er entwickelte die moderne strategische Implantologie. Unter seiner Führung wurde die Simpladent-Klinikgruppe undein Ausbildungssystem für Implantologen und das Sofortbelastungskonzept aufgebaut.</p>
