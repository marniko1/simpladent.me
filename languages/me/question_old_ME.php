<h2>Koje su tri najvažnije prednosti Simpladent® tretmana sa <a href="http://www.strategic-implant.com" target="_blank">Strategic® implantatima</a>?</h2>
<ul style="margin-left:10px;">
<li><strong>1.	Brzina:</strong> <br />
U samo jednoj posjeti uklonjeni su zubi i odmah zatim ugrađeni Strategic® implantati. Za razliku od konvencionalnih metoda, implantati se  ugrađuju u kortikalnu kost. Potrebna je mala količina ove kosti da se osigura uspjeh tretmana, tako da su procedure koštane augmentacije, koštani transplantati i sinus lift nepotrebne i zastarjele. Ušteda u vremenu iznosi 98% cjelokupnog vremena potrebnog za tretman i značajno se smanjuje diskomfort i bol za pacijenta.</li>
<br />
<li><strong>2.	Sigurnost:</strong> <br />
Svaka kiruška procedura, koje možemo poštedjeti pacijenta, smanjuje ukupan rizik za pacijenta. Isključujući koštane augmentacije, transplantate i druge procedure, rizik je minimaliziran primjenom Simpladent® metode: potrebna je samo jedna kiruška intervencija.</li>
<br />
<li><strong>3.	Bezbolnost:</strong> <br />
Nakon uklanjanja zuba, pacijenti mogu očekivati da se javi uobičajena bol izazvana samim uklanjanjem zuba. Međutim, ne treba očekivati nikakavu dodatnu bol zbog ugradnje implantata. Zbog toga, sam proces ugradnje implantata je bezbolan.</li>
</ul>
