<br />

<h2>Kakvom se pokazala Simpladent® metoda u usporedbi sa drugim tretmanima imedijatnog opterećenja, fiksiranih zuba u roku od tri dana ili „All-on-4“?</h2>

„All-on-fo-4“ metoda podrazumijeva ugradnju četiri konvencionalna implantata u čeljust pod određenim kutem. (Slično „All-on-6“ predstavlja šest implantata usidrenih pod kutem). Ova metoda nosi brojne rizike: zbog malog broja implantata i popuštanja spojeva, frakture implantata se mogu očekivati tijekom vremena. Samo jedan frakturirani implantat zahtjeva zamjenu cijele konstrukcije. To dovodi do povećanja troškova i produžava vrijeme potrebno za tretman. Budući da mastikatorni centar nije zamjenjen, korektno žvakanje postaje teško. Dodatno, rizik od periimplantitisa ostaje visok, pogotovo za rizične grupe, kao što su pušači i dijabetičari. 
<br />
Koncept Simpladent® tretmana zahtjeva ugradnju adekvatnog broja kortikalnih-bazalnih implantata u čvrste dijelove kosti za optimalnu stabilnost i dugotrajne rezultate. Oni su konstruirani za imedijatno opterećenje. 
