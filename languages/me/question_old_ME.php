<h2>Moj doktor mi je objasnio da mi je neophodna augmentacija kosti prije ugradnje implantata.</h2>

Doktori educiraju pacijente prema vlasitom znanju koje posjeduju. Sve dok se oni drže konvencionalne implantologije, dodatne procedure (kao što su koštane augmentacije sa dugim periodima čekanja i zarastanja) bit će neophodne, uprkos boli i neugodi pacijenata i uprkos činjenici da je uspjeh tretmana nesiguran zbog mnogobrojnih intervencija. Danas pacijenti imaju izbor da odbiju konvencionalne tretmane i da potraže alternativu.
<br />
Strategic® implantati omogućavaju rad sa pacijentima koji imaju vrlo malo raspoložive kosti, jer su polirani, jednodjelni implantati i ugrađuju se u stabilnu kost koju svaki pacijent ima u dovoljnoj količini. Čak i pacijenti sa postojećim periimplantitisom, oboljenjima periodoncijuma ili gingivitisima mogu dobiti implantte imedijatno koristeći Simpladent metodu. Rezultat je predvidljiv i pouzdan.
