<h2>Kakva je situacija  sa „pacijentima rizika“ (kao što su pušači, dijabetičari ili oboljeli od drugih bolesti)? Da li se njima mogu ugraditi Strategic® implantati?</h2>

Da, uvijek. 
<br />
Upotreba konvencionalnih dvofaznih implantata je povezana sa velikom vjerojatnošću poduzimanja dodatnih kirurških tretmana. To je opterećenje za pacijenta. Uz to, uvijek je prisutan rizik od periimplantitisa, ozbiljne infekcije za koju trenutno nema efikasnog tretmana. Ovi problemi ne pogađaju pacijente tretirane Simpladent® metodom. Neophodna je samo jedna kirurška intervencija u toku koje se ugrađuju polirani jednodelni implantati. Slučajevi periimplantitisa nisu poznati.
<br />
<br />

<h2>Što je periimplantitis i što možemo poduzeti?</h2>
Što su duže u upotrebi konvencionalni dvofazni implantati sa velikom, hrapavom površinom, veće su šanse za infekciju periodoncijuma i u okolini implantata. Infekcija je poznata kao periimplantitis i povezana je sa gubitkom kosti. Napredovanje infekcije i posljedično smanjenje kvalitete života pacijenta dovodi do uklanjanja implantata kasnije. Terapija konvencionalnim implantatima nije uspjela.
<br />
Periimplantitis se javlja u 50% tretiranih slučajeva, često nakon vremena potrebnog za zarastanje implantata, onda kada sve izgleda dobro i pacijent počinje koristiti implantate za žvakanje.
<br />
Trenutno ne postoji efikasan tretman ovog fenomena. Svi pokušaji čišćenja zahvaćenih dijelova ne daju rezultate, jer se milioni novih bakterija konstantno stvaraju u ustima. Jedini siguran način da se pobijedi infekcija je uklanjanje konvencionalnih implantata i ugradnja Strategic® implantata.
<br />
Detaljne informacije o ovoj temi su dostupne ovjde:  <a href="http://www.peri-implantitis.info/en/" target="_blank"> www.peri-implantitis.info </a>
