<h2>Imam problema sa svojim konvencionalnim implantatima. Da li ih mogu zamjeniti Simpladent® metodom?</h2>

Da, naravno.
<br />
Troškovi tretmana konvencionalnim 2-faznim implantatima se nekontrolirano uvećavaju tijekom mjeseci ili čak godina. Jedna kirurška intervencija, zatim sljedeća, postoperativna njega...račun se uvećava. Pacijenti se boje završiti tretman ili nisu svjestni alternativnih tretmana. Pacijenti se osjećaju kao da nema kraja.
<br />
Mnoge kirurške procedure, kao što su koštani transplantati, nepotrebne su ako se primjenjuje naša metoda. Budući da smo u mogućnosti izbjeći ove procedure, kao rezultat imamo smanjenje troškova od 30% u odnosu na konvencionalne metode. Dodatnih 20% pacijenti mogu uštedjeti izbjegavanjem privremenih protetskih radova, koji su također nepotrebni kada se radi u protokolu imedijatnog opterećenja. 
Promjena metode tretmana se gotovo uvijek isplati. Konvencionalni implantati su uklonjeni i postavljeni su novi Strategic® implantati u jednoj kirurškoj intervenciji. Pacijenti se mogu vratiti normalnom životu sa osmjehom nakon tri do pet dana, najviše.
