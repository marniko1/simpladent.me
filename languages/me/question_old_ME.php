<h2> Da li sam kandidat za Strategic® metodu ugradnje implantata?</h2>

Mnogi pacijenti si postavljaju ovo pitanje,pogotovo pacijenti kompromitiranog zdravlja ili pacijenti sa kroničnim bolestima (kao što je dijabetes) ili strastveni pušači.<br />
<br />
Odgovor je jednostavan: Da! Preko 99% pacijenata je uspješno tretirano sa Strategic® implantatima.<br />
<br />
Uključujuci pacijente sa oboljenjima periodoncijuma i (kroničnim) gingivitisima, kao i pacijente koji su odustali od svojih fiksnih zuba zbog dugotrajnog,  mukotrpnog  ili preskupog tretmana.<br />
<br />
Svi ovi pacijenti mogu riješiti problem nedostatka zuba „samo jednim tretmanom“.
<br />
