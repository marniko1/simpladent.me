<h2>Koja je slaba strana Simpladent<sup>®</sup> metode?</h2>

Nema je, sa medicinskog aspekta postoje samo prednosti: brza je, sigurna, pristupačna i predvidljiva. Nema skrivenih “trikova”. <br />
<br />
U rijetkim slučajevima, željeni estetski rezultati se ne mogu ostvariti odmah. Uslijed rada u protokolu imedijatnog opterećenja (48 do 72 sata) vrijeme za kontroliranje estetskih rezultata je ograničeno. Naši tehničari su obučeni i za ove slučajeve.
<br />
<br />
Prirodan proces ponovne izgradnje koštanog tkiva se očekuje poslije vađenja zuba. Kod pacijenata sa vrlo malom količinom koštanog tkiva, koji ne mogu biti tretirani konvencionalnim implantatima, može se vremenom pojaviti mali razmak između desni i mosta. Ovo može biti korigirano podlaganjem ili zamjenom prvog mosta.
<br />
<br />
Stvaranje i remodelacija kosti se završava poslije približno 6 do 9 meseci. Nakon ovog perioda stabilnost implantata neće biti ugrožena zamjenom mosta, tako da je to moguće poslije ovog vremena. 
