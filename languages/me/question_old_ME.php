	<li>U radu sa pacijentima koji imaju veliku količinu koštanog tkiva (vertikalno i horizontalno), dobri estetski rezultati mogu se postići svakom metodom. </li>
	<br />
	<li>Ako pacijent ima veoma malu količinu koštanog tkiva (konvencionalni tretman je nemoguć, ne preporuča se ili je moguć jedino uz prethodne ekstenzivne i rizične procedure augmentacije kosti), Simpladent metoda i u ovom slučaju može ponuditi uspješan tretman u protokolu imedijatnog opterećenja. </li>
	<br />
	<li>U pojedinim slučajevima su neophodni kompromisi u estetskim rješenjima, zbog veoma reducirane količine kosti i promjenjenog položaja oralne sluzokože. U ovim slučajevima specijalisti mogu sugerirati dodatne terapijske korake, individualno primenljive.</li>
</ul>
