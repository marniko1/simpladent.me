        <h1>IMPLANTOLOGIJA</h1>
        <div class="article">
            <h2>Što je "Bazalna implantologija" / Strategic implantologija?</h2>
            <p>Termin "bazalna implantologija" pojavio se prije više od 15 godina. Tada je počeo napredak u modernoj dentalnoj implantologiji. Razvijene su tehnologije za savladavanje najtežih slučajeva atrofije kada se činilo da ne postoji način da se pomogne pacijentu. Danas I pacijenti sa dijebetesom I ostalim medicinskim problemima mogu biti tretirani uz pomoć Strategic implantata.</p>
            <p>Svaki implantat je fiksiran na najmanje dva mjesta u kortikalnoj kosti. Ova kost posjeduje visok nivo mineralizacije. Implantati se ugrađuju isključivo u kost velike gustoce, koju svi posjeduju u dovoljnoj količini.</p>
            <p>Kortikalna kost je veoma pogodna za transmisiju opterećenja zahvaljujući visokom nivou mineralizacije. Pruža mogućnost za rad u protokolu imedijatnog opterećenja. Prema tome, čak I u slučajevima velikog gubitka kosti I dugogodišnje potpune bezubosti, pacijent može imati prekrasan osmeh u roku nekoliko dana, maksimalno 3.</p>
        </div>
    </div>

    <div class="note">Više od 1 000 000 implantata je ugrađeno, na tisuce zadovoljnih pacijenata iz nemjerljivog broja zemalja širom svijeta.</div>
    <div class="benefits">
        <div class="article">
            <h2>6 prednosti Strategic Implantata:</h2>
            <ol>
                <li>Svi pacijenti kojima su implantati neophodni mogu biti tretirani (bez izuzetaka) i bez obzira na stupanj gubitka kosti i prisustsvo ili nedostatak vlastitih zuba.</li>
                <li>Potrebna je samo jedna intervencija. Strategic Implantati su povezani (trajnim ili privremenim) mostovima ubrzo nakon ugradnje. Ukoliko je potrebno, drugi most (po želji može biti napravljen od drugog materijala) moguće je postaviti 6-9 meseci kasnije. U većini slučajeva prvi most je ujedno I definitivni.</li>
                <li>Nizak broj kirurških I ostalih tretmana značajno smanjuje ukupne troškove u usporedbi sa ostalim tradicionalnim metodama. Brze I efikasne mjere će biti poduzete od strane naših specijalista ukoliko dođe do bilo kakavih komplikacija.</li>
                <li>Implantati malog dijametra gotovo uopce ne utječu na dotok krvi u kost, u svakom slučaju mnogo manje u usporedbi sa konvencionalnim implantatima.</li>
                <li>Budući da se Strategic Implantati mogu (moraju) imedijatno opterećivati, pacijenti se izuzetno brzo mogu vratiti svakodnevnom životu.</li>
				<li>Implantati poliranih površina su gotovo oslobođeni opasne infekcije oko implantata: nije pronađen periimplantitis (vidjeti:<a href="http://www.peri-implantitis.info/en/" target="_blank"> www.peri-implantitis.info</a> da se informirate o ovom problemu koji je povezan sa konvencionalnim dentalnim implantatima).</li>
