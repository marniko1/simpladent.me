<h2>Koja je bitna razlika između konvencionalne implantologije i Simpladent<sup>®</sup> metodologije?</h2>

Konvencionalna implantologija sa dvofaznim implantatima zahtjeva obilje koštanog tkiva, vremjena i novca. U mnogim slučajevima za proceduru, pacijenti ne poseduju dovoljno kosti, što rezultira potrebom za koštanim augmentacijama, sinus liftom ili riskantnim koštanim transplantatima. Cjelokupan tok tretmana karakterizuje dugi period čekanja, dugo vrijeme zarastanja i visoke troškove.<br />
<br />
Ovdje postaje jasno izražena prednost Simpladent implantologije, koja se pokazala pouzdana i predvidljiva.<br />
<br />
Čak i kod pacijenta sa veoma malo koštanog tkiva može da uslijedi imedijatni tretman poliranim, jednodjelnim, kortikalnim-bazalnim implantatima. Za našu metodu potrebna kortikalna kost se nalazi u dovoljnoj mjeri, u svakoj vilici. Ove stabilne, kortikalne kosti su veoma pogodne za ugradnju implantata i dostupni su kod praktično svakog pacijenta, tokom cijelog života. Strategic Implant<sup>®</sup> se može odmah opteretiti. 
<a href="http://www.peri-implantitis.info/de/" target="_blank">Periimplantitis</a> koga sa pravom se bojimo, jedna opasna infekcija, se kod ove metode ne pojavljuje.<br />
