				<li>Periimplantitis je jedna teška infekcija, koja se pojavljuje isključivo nakon implantacija konvencionalnom metodom i vodi do ozbiljnih problema sa gubitkom kostiju vodi. U postupku imedijativnog opterećenja do sada se ovo nije moglo primetiti. Dodatne informacije videti na <a href="http://www.peri-implantitis.info/de/" target="_blank">www.peri-implantitis.info</a>.</li>
