    <div class="columns">
        <div class="col">
            <div class="article">
                <h2>Indikacije za Strategic Implantate</h2>
                <ul>
                    <li>Situacija, u kojoj zubi djelimično ili potpuno nedostaju i u kojoj se žele imati čvrsti vještački zubi.</li>
                    <li>Nakon gubitka prvobitnih dvo-faznih-implantata (takođe nakon periimolantitis) odnosno, nakon gubitka augmentacije kostiju </li>
                    <li>U situaciji tretmana kod jako ili malo smanjene atrofije kostiju  do veoma izražene atrofije</li>
                    <li>ovi implantati se mogu primjeniti sa istom vjerovatnošću na uspjeh kod pušača kao i kod nepušača</li>
                    <li>Dijabetes i ostale teške sistemske bolesti kod ovih implantata ne igraju nikakvu ulogu</li>
                </ul>
            </div>
        </div>
        <div class="col">
            <div class="article">
                <h2>Kontraindikacije:</h2>
                <ul>
                    <li>Produžena oralna ili i.v. Bisfosfonata (funkcija koštanog sistema mora biti provjerena prije tretmana implantatima)</li>
                    <li>U slučajevima, u kojima bilateralnu funkciju žvakanja nije moguća stvoriti, npr. kod nejednakog funkcionisanja mišića i kod parcijalnog paralizovanja  mišića , odnosno posle nakon kidanja (dissektion) mišića. Ovi slučajevi  mogu ipak pod određenim okolnostima da budu od iskusnih specijalista tretirani.</li>
