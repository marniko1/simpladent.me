        <h1>Povijest</h1>
        <div class="article">
            <p><strong>Dr.IhdeDental</strong> je osnovao zubni tehničar Klaus Ihde 1954. godine u Berlinu. Od dana osnivanja kompanija se profilirala kao dobavljač visokokvalitetnog stomatološkog potrošnog materijala. Krajem osamdesetih godina kompanija se transformirala u <strong>Dr.IhdeDental</strong> grupu I započinje sa vlasitom proizvodnjom.</p>
            <p>IhdeDental grupa je jedna od vodećih svjetskih kompanija za proizvodnju implantata za rad u protokolu imedijatnog opterećenja. Danas se proizvodi ove kompanije koriste u više od 40 zemalja. Proizvedeno je više od 1 000 000 implantata u međuvremenu. Ranije proizvodnja nije bila organizirana u kompaniji, međutim danas se sve faze proizvodnje odvijaju u glavnoj kompaniji u Švicarskoj.</p>
            <p>IhdeDental je razvio implantate koje je moguće koristiti čak I u neobičnim slučajevima I situacijama gde nema dovoljno koštane strukture. To je transplantaciju kosti I postupak augmentacije učinilo nepotrebnim.</p>
            <p>Lider kompanije je Prof.Dr.Stefan Ihde, stomatolog & oralni kirurg, koji je stomatološko obrazovanje stekao u Zapadnoj Njemačkoj (1982-1987). On nije razvio samo implantate za modernu dentalnu implantologiju, već je definirao i kategorizirao metode  koji se koriste širom svijeta od strane doktora educiranih za ovaj sistem. Pod njegovim vodstvom je osnovana Simpladent asocijacija dentalnih klinika iz cijelog svijeta. </p>
