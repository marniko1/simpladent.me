        <h2>Sta Vam možemo ponuditi?</h2>
        <p>Naš tretman koncipiran je tako da riješi sve Vaše probleme odjednom, zube koji nedostaju, nesposobnost da normalno jedete I smijete se, periodontalne probleme (upale desni i mobilnost zuba), endodontske probleme (granulome I ciste), kao i probleme sa teško pokvarenim ili rekonstruiranim zubima.</p>
      
        <p>Obično pristupamo rješavanju svih problema u jednom koraku. Vađenje zuba I ugradnja implantata se obavljaju u jednoj posjeti I odmah nakon toga naš labaratorijski tim počinje raditi na Vašim novim zubima. Kako bi se izbjegli problemi u budućnosti, odstranjuju se također zubi sa bolom i zubi za koje postoji velika vjerojatnost da bi mogli uzrokovati probleme u periodu od narednih 5 godina, te se zamjenjuju implantatima.</p>
        <p>Naše iskustvo stečeno tisucama implantoloških operacija jasno pokazuje da su implantati kod odraslih pacijenata mnogo pouzdaniji od njihovih vlastitih zuba.</p>
