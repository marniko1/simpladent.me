        <h1>History</h1>
        <div class="article">
            <p><strong>Dr.IhdeDental</strong> was founded in 1954 by the dentist technician Klaus Ihde in Berlin. From the very first days of its existence the company has recommended itself as a supplier of high 
			quality consumables for dentistry. At the end of the 80ies the  trading company was transformed into <strong>Dr.IhdeDental</strong> group and manufacturing started.</p>
            <p>IhdeDental Group is one of the world's leading manufacturers of dental implants for immediate loading procedures. Today its products are used in more than 40 countries. More than 1.000,000 implants were 
			produced for the immediate loading procedure in the meantime. While in the early years the production was not done inhouse the group has now integrated almost all steps for production into the main company in Switzerland</p>
            <p>IhdeDental has developed implants which work even in unusual cases and in situations with very low bone. Thereby bone transplants and augmentations became fully unnecessary.</p>
            <p>The scientific leader is today Prof. Dr. Stefan Ihde, dentist & oral surgeon, with dental education from West-Germany (1982-1987).
			He has not only developed the devices for modern dental implantology but also defined and categorized the methods which are used world-wide by the trained users of the system world wide. Under his leadership a worldwide Simpladent
			association of dental clinics was formed.</p>
