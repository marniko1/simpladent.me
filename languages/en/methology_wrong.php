        <h1>MITOVI & ISTINE</h1>
        <div class="myths">
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">mit:</div>
                        <div class="text">Dentalni implantati se ne mogu imedijantno opterećivati</div>
                    </div>
                    <div class="reality">
                        <div class="header">Istina:</div>
                        <div class="text">Vise od 20 godina moguće je optrećivati napredne tipove dentalnih implantata imedijantno. Ipak, sa vodećim brendovima tradicionalnih (istorijskih) implantata (Straumann,Branemark,Nobel Biocare etc) protokol imedijantnog opterećenja moguć je samo u nekoliko povoljnih okolnosti. Sa modernim bazalnim implantatima rad u protokolu imedijantnog opterećenja je moguć gotovo uvek.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">mit:</div>
                        <div class="text">Jos je jedan mit da Strategic implantologija, pošto zahteva manje vremena, pruža lošiji kvalitet tretmana.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Istina:</div>
                        <div class="text">Strategic implantati su sigurniji za upotrebu od konvencionalnih (istorijskih) dvofaznih implantata, jer omogućavaju imedijantno opterećenje. Rad u protokolu imedijantnog opterećenja ne zahteva vreme zarastanja.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">mit:</div>
                        <div class="text">Implantati moraju biti bez opterećenja kako bi pravilno zarasli</div>
                    </div>
