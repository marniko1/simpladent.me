                    <h1>Pricing</h1>
                    <div class="article">
                        <p>The Simpladent treatment allows you to save money. Not by using cheap implants or dupe accessories, but with the help of modern, anvanced and effective technology. Compared to conventional methods, our treatment expenses only amount to approximately two thirds of the price due to fewer appointments in the clinic, fewer treatment steps and almost no medications.</p>
