<div class="header">Example:</div>
<div class="desc">
<p>Der exact price depends on the applied implant system and the patient's individual needs. 
 Three examples show different possibilities:
1. A front tooth or premolar are replaced by one Strategic Implant. The costs for this treatment amount to about 500 Euro.<br />  
2. A lower 1st molar is replaced by two Strategic Implants. The costs amount to around 750,-- Euro. <br /> 
3. A lower or upper 2nd molar or an upper 1st molar are placed by 3 Strategic Implants in an immediate loading protocol. The costs for this treatment will be around 950 Euro.<br />
Costs of the prosthetic treatment are added.</p>  
<p>The price for the bridge or crown depends on the manufacturing process and desired material.</p>
<p>Duration of treatment: Usually 2 - 5 days</p>
<p>&nbsp;</p>
<p>*The final price depends  on the difficulty of the case and the necessary effort. It can be (even significantly) lower than mentioned here.  Terms of payment must be agreed with the center. The final price also depends on the chosen material of the bridge (Metal-Acryl, Metall-Composite, Metall-Ceramik, Zirconium).</p>
<p>*The final price for the treatment will be calculated after an X-ray and the medical history are available for the treatment provider. Different centers may have different calculations.  The differences in pricing are connected with the equipment of the clinic, the location and with the sum of services offered.</p>
<p>*The treatments are typically offered in Euro. Payment however is done in most countries in local currency.</p>
