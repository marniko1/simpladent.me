    <div class="columns">
        <div class="col">
            <div class="article">
                <h2>Indications for strategic implants:</h2>
                <ul>
                    <li>all kinds of situations when several teeth are missing or have to be extracted</li>
                    <li>when the procedure of 2-stage implant placement or bone augmentation  has failed</li>
                    <li>all kinds of bone atrophy- from the light cases up to very severe stages</li>
                    <li>treatment of smokers is possible with the same chance of success</li>
                    <li>Diabetes and other serious diseases, which slow down the healing process.</li>
