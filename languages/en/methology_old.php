        <p>1. Inspection of the oral cavity and x-ray examination are done by the treatment provider. After all questions have been answered the treatment date is agreed. 
		2.The treatment  is often begun immediately because the precuations necessary for conventinal implants (pre-operative improvement of oral hygiene, periodontal pre-treatment, etc) are not necessary for the Strategic Implant &reg;, if the involved teeth are simply extracted.</p>
