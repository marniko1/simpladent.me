        <h1>Implantology</h1>
        <div class="article">
            <h2>What is “Basal Implantology” / Strategic Implantology?</h2>
            <p>The term "basal implantology" appeared more than 15 years ago. At this time a breakthrough in modern dental implantatology started. The technology was developed to cope with the most difficult cases of strong atrophy and when it seemed that there was no other way to help the patient. Today also diabetic patients and patients with other medical issues may receive treatment through basal implants.</p>
            <p>Every implant is fixated at least in two locations in cortical bone. This type of bone provides a high level of mineralization and stability.  The implants are anchoured exclusively into dense bone, of which everybody has enough. </p>
            <p>Cortical bone is ready and capable for load transmission, due to it's high level of mineralization. It gives a realistic opportunity for immediate loading protocols. Thus, even in cases of severe bone loss, and if all teeth a re missing for a long time the patient can get a beautiful smile after within a few days and max. 3 days!!</p>
        </div>
    </div>

    <div class="note" style="float:right;">More than 1.000,000 implants were placed, thousands of satisfied patients from an uncountable number of countries around the world.</div>
    <div class="benefits">
        <div class="article">
            <h2>6 advantages of Strategic Implantology:</h2>
            <ol>
                <li>All patients which request the implants can be treated (without exception) regardless of the extent of bone loss or the presence of absence of residual teeth.</li>
                <li>Only one single intervention is necessary. Strategic Implants are splinted with (permanent or semi-permanent) bridges very soon after the implant placement. If requested, another bridge (maybe from a different material) may be placed 6-9 months later. In many cases however the first bridge will be the final one.</li>
                <li>The low number of surgical and other treatment steps reduces overall cost compared to other traditional methods. Rapid and effective measures can be taken by our qualified specialists if complications occur.</li>
                <li>Implants with slim design hardly interfere with the blood supply of the host bone, at any rate much less than comparable conventional implants.</li>
				<li>The polished implants are virtually free from the dangerous infection around implants: no peri-implantits is found (see: www.peri-implantitis.info to learn more about this problem, which is connected to conventional dental implants) </li>
                <li>Because Strategic Implants can (must) be immediately loaded, and therefore the patients can quickly return back to their normal life.</li>
