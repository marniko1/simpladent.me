        <h1>Myths & Reality</h1>
        <div class="myths">
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">Dental implants can not be loaded immediately</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">Since more than 20 years it is possible to load some advanced designs of dental implants immediately. However with the leading brands of traditional implants this treatment protocol works only under a few, favourable circumstances. With basal implants it almost works always.<br /> With modern basal implants almost all patient cases may be treated in immediate loading protocols.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>  
                        <div class="text">Because the Strategic Implant &reg; requires less healing time, the quality of the treatment is reduced.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">The Strategic Implant &reg; is more safe to use than conventional (historical) 2-stage implants, because it works in immediate loading.<br /> Because they are immediately loaded. Immediate load protocols require no unloaded healing time.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">Implants have to be non-loaded to heal-in well.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">Bone heals only if activated by micro-movements. These movements are directing the process of healing. Chewing on the Strategic Implant &reg; provides such an adequate functional stimulus for directed healing. </div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">The Strategic Implant &reg; is used only in rare cases with severe atrophy.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">The Strategic Implant &reg; works well in cases of severe atrophy (nothing else works in such cases) and it works also just as well in cases with good bone supply and good bone quality. The Strategic Implant &reg; works for virtually everybody.</div>
