                    <h1>Treatment possibilities and costs of treatment</h1>
                    <div class="article">
                        <p>The Simpladent treatment allows you to save time and money. Not by using cheap implants or dupe accessories, but with the help of modern, anvanced 
						and effective technology. Compared to conventional methods, the treatment expenses only amount to approximately two thirds of the price of comparable 2-stage treatments 
						due to fewer appointments in the clinic (usually only three for the initial treatment), fewer treatment steps and faster treatment progress. The final price for the individual treatment will be defined after x-rays and medical documentation have been submitted to the treatment provider and after he/she knows about the individual needs.</p>
