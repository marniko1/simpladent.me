	<p>The treatment examples shown here provide only an overview on the treatment possibilities.  Not in all clinical cases the same results are possible.  
Note that the appearance of the new teeth depends also on the available volume of the underlying bone and the volume of the soft tissue.
	Implants do not replace bone or soft tissues. 
	 Implants create a stable anchorage for the placement of crowns and bridges. The Strategic Implant<sup>®</sup> is typically suitable for use without bone augmentation. It works with the available rests of the bone.  </p>
