        <h1>Myths & Reality</h1>
        <div class="myths">
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">Dental implants can not be loaded immediately</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">It has been possible to load some advanced designs of dental implants immediately for over 20 years. Unfortunately this treatment protocol isn't possible with the leading brands of traditional implants, or only works under very few favorable circumstances. With modern basal implants almost all patient cases are treated successfully.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>  
                        <div class="text">Because treatment with the Strategic Implant is faster, the quality must be lower.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">Strategic Implants &reg; are much safer than conventional 2-stage implants. Immediate load protocols simply do not require comparable healing time since cortical bone can be immediately loaded.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">Implants have to be non-loaded to heal-in well.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">Bone heals only if activated by micro-movements. These movements direct the healing process. Chewing on the Strategic Implant &reg; provides such an adequate functional stimulus for directed healing. </div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">The Strategic Implant &reg; is used only in rare cases with severe atrophy.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">The Strategic Implant &reg; works equally well in cases of medium to severe atrophy and with good bone supply and good bone quality. It is also a popular choice when immediate loading or affordable treatment are desired.</div>
