        <h1>Immediate Loading Implantology Today</h1>
        <div class="article">
            <h2>What are Basal Implantology and Strategic Implantology?</h2>
            <p>The term "Strategic Implant" is a registered trademark for the implants of the Simpladent Group. The term "basal implantology" first appeared around 1999; at a time of breakthrough in modern dental implantology: immediate loading technology was developed. It simultaneously solved the problem of low bone supply and enables patients with diabetes and other medical issues to receive treatment.</p>
            <p>Each implant is fixated in at least two locations in cortical bone. This type of bone provides a high level of mineralization and stability, can be immediately loaded and practically does not resorb in a patient's lifetime. Bone augmentation, sinus lift, bone transplants and waiting periods become unnecessary. </p>
            <p>With the help of the Strategic Implant, we are able to successfully supply treatment even in cases of severe bone atrophy. The patient receives a beautiful smile within just a few days.</p>
        </div>
    </div>

    <div class="note">Thousands of patients have been treated globally with over 1.000,000 placed implants.</div>
