    <h1>Indications and Contra-indications</h1>
    <div class="columns">
        <div class="col">
            <div class="article">
                <h2>Indications for strategic implants:</h2>
                <ul>
                    <li>partly or completely missing teeth, or when fixed bridges are desired</li>
                    <li>when the procedure of 2-stage implant placement or bone augmentation  has failed</li>
                    <li>slight bone loss up to severe bone atrophy</li>
                    <li>treatment of smokers is possible with the same chance of success</li>
                    <li>Diabetes and other serious diseases that delay the healing process.</li>
                </ul>
            </div>
        </div>
        <div class="col">
            <div class="article">
                <h2>Contraindications:</h2>
                <ul>
                    <li>prolonged oral or i.v. use of Biphosphonates </li>
                    <li>cases where bilateral equal mastication can not be arranged, e.g. when chewing muscles or their innervation are partly missing. However, these cases might still be treatable by experienced professionals. </li>
                </ul>
