            <ol>
                <li>Лечение могут пройти все пациенты, желающие поставить имплантаты, независимо от степени потери кости, а также наличия или отсутствия остаточных зубов.</li>
                <li>Требуется только одна операция. Базальные имплантаты  шинируются (постоянно илу полупостоянно) при помощи мостов в сскором времени после установки имплантатов. По желанию, окончательный мост может быть установлен через 10-15 недель. Во многих случаях первый мост - это уже окончательный.</li>
