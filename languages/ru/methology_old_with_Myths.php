        <h1>Имплантология</h1>
        <div class="article">
            <h2>Что такое "базальная имплантология"?</h2>
            <p>Термин "базальная имплантация" появился более 20 лет назад. И это стало прорывом в современной дентальной имплантологии. Эта технология была разработана для особо сложных случаев, когда помочь пациенту казалось невозможным. На сегодняшний день даже пациенты с диабетом и другими медицинскими проблемами могут получить лечение на базальных имплантатах.</p>
            <p>Каждый имплантат крепится по крайней мере в двух местах в кортикальной кости. Этот тип кости имеет высокую степень минерализации. Имплантаты избирательно фиксируются в плотной кости, которая есть у каждого.</p>
            <p>В связи с высоким уровнем минерализации кортикальной кости, она  очень хорошо воспринимает нагрузку. Это дает возможность осуществлять немедленное протезирование и устанавливать мост сразу же после имплантации. Таким образом, даже при тяжелой степени потери кости и при полном отсутствии зубов пациент может получить красивую улыбку уже через 3 дня!</p>
        </div>
