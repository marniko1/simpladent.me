        <p>Установка имплантат начинается с осмотра состояния полости рта и получение панорамного снимка/3D компьютерной томограммы челюстей. После обсуждения  всех вопросов, согласовывается дата лечения. К лечению зачастую можно приступить  немедленно, потому что предварительные этапы, необходимые при традиционной имплантации (улучшение гигиены полости рта, предварительное лечение пародонта и т.д.) не являются необходимыми для базальных имплантатов, т.к. пораженные зубы просто удаляются.</p>
