        <h1>История</h1>
        <div class="article">
            <p><strong>Компания Dr.IhdeDental</strong>  была основана в Берлине в 1954 году зубным техником Клаусом Иде. С первых же дней существования компания зарекомендовала себя как поставщик высококлассных расходных материалов для стоматологии. В конце 80-ых торгующая компания постепенно стала заниматься производством товаров.
</p>
            <p>На сегодняшний день Dr. IhdeDental Group – это один из ведущих мировых производителей имплантатов для немедленной нагрузки. Ее продукцию используют в более чем 40 странах мира. С тех пор произведено более 1 000 000 имплантатов, которые применяются по всему миру. В первые годы производство не было штатным, позже Группа Dr. IhdeDental внедрила почти все этапы производства в Швейцарии.</p>
            <p> Компания Dr. IhdeDental разработала имплантаты, которые дают замечательные результаты даже в очень сложных ситуациях. Для их установки достаточно минимального объема кости, поэтому нет необходимости в трансплантации и наращивании кости.</p>
            <p>Научный руководитель Группы Dr. IhdeDental, Профессор доктор Стэфан Идэ, получивший стоматологическое образование в Германии (1982-1987), дальнейшую специальзацию прошел по периодонтологии и оральной хирургии. Он разработал современную Strategic имплантологию. Под его руководством были сформированы: Ассоциация стоматологических клиник Simpladent, система обучения для имплантологов и концепция немедленной нагрузки.</p>
