        <div class="article">
            <h2>Что такое Strategic Implant<sup>®</sup>?</h2>
            <p>Термин “Strategic Implant<sup>®</sup>” является зарегистрированным торговым знаком для имплантатов Группы Simpladent.  Термин "базальная имплантология" впервые появился примерно в 1999 году, в то время, когда произошел прорыв в современной дентальной имплантологии - тогда была разработана технология немедленной нагрузки. Она одновременно решила проблему недостаточного количества кости и дала возможность получить лечение пациентам с диабетом и другими медицинскими проблемами</p>
            <p>Каждый имплантат крепится, по крайней мере, в двух местах в кортикальной кости. Эта кость имеет высокую степень минерализации и стабильность, ее можно сразу нагружать, и она практически не подвергается атрофии в течении жизни. Наращивание кости, синус-лифтинг и длительные периоды ожидания перестали быть необходимостью.</p>
            <p>С применением Strategic Implant<sup>®</sup> мы можем успешно проводить лечение даже в случаях выраженной атрофии кости. Пациент получает красивую улыбку всего за несколько дней.</p>
