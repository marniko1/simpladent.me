        <h1>History</h1>
        <div class="article">
            <p><strong>Dr.IhdeDental</strong> was founded in 1954 by the dentist technician Klaus Ihde in Berlin. From the very first days of its existence the company has recommended itself as a supplier of high quality consumables for dentistry. At the end of the 80th the  trading company was transformed into <strong>Dr.IhdeDental</strong> group and manufacturing started.</p>
            <p>Dr IhdeDental company is one of the world's leading manufacturers of dental implants for immediate loading procedures. Today its products are used inmore than 40 countries. More than 100,000 implants are produced annually at the company's factory in Switzerland.</p>
            <p>Dr. IhdeDental has developed implants which work even in unusual cases and in situations with very low bone. Thereby bone transplants and augmentations became unnecessary.</p>
            <p>Companies leader is a Prof. Stefan Ihde, a dentist with dental education from West-Germany. He is also periodontologist and oral surgeon, as well as the developer and founder of modern basal implantology. Under his leadership a worldwide  Simpladenе association of dental clinics was formed.</p>
