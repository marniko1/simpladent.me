        <h2>What can we offer for you?</h2>
        <p>Our treatment concept aims at solving all of your problems at the same time, such as:</p>
        <ul>
	    <li>missing teeth, inability to eat and smile; </li>
            <li>periodontal problems (inflammation of the gums, tooth mobility); </li>
            <li>endodontic problems (granulomas, cysts); </li>
            <li>problems with heavily decayed or reconstructed teeth;</li>
            
        </ul>
        <p>Typically we approach all those problems in one step. Removal of teeth and placement of implants are done in the same appointment, and right after this our lab-team starts working on your new teeth. To avoid future problems, also ailing teeth, i.e. teeth which are likely to cause problems within the next 5 years, will also be removed an replaced by implants.</p>
        <p>Our experience out of thousands of implant operations and case observations clearly shows, that implants are in adult patients much more relyable than teeth. </p>
