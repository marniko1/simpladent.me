            <h2>What are the advantages of the Simpladent concept?</h2>
            <p>The Simpladent-concept has changed the world of implantology completely!.</p>
            <p> in our clinics you will become free of all dental worries and will be back in your normal life very quickly.</p>
            <ul>
                <li>Every patient can get fixed teeth right away, without waiting out the healing period and without without bone augmentations or intermediate dentures.</li>
                <li>Our extensively trained surgeons utilize cortical bone in all areas of the maxillo-facial skeletton. This bone is resorption-stable and strong. For this reason we install bridges right away and splint the implants thereby.</li>
                <li>Specialized dentist-technician will give you a sound and safe bite through these bridges. You may eat everything right after the bridges are installed!</li>
