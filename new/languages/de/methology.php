        <h1>Sofortimplantologie</h1>
        <div class="article">
            <h2>Was ist “Basale Implantologie”?</h2>
            <p>Der Begriff  "basale Implantologie" wurde ca. im Jahre 1999 ertsmals verwendet. Zu dieser Zeit begann einer der grössten Durchbrüche in der dentalen Implantologie.  Die Technologie der Sofortbelastung wurde entwickelt. Damit wurde yugleich das Problem des reduyierten Knochenangebots gelöst und mit Hilfe von basalen Implantaten können heute auch Diabetiker und andere Patienten mit Begleiterkrankungen behandlet werden.</p>
            <p>Jedes Implantat wird mindestens an zwei Orten im Kortikalknochen verankert.   Dieser Knochen weist hohe Stabilität auf, er vermag sofort die Kaulast aufzunehmen und er resorbiert im Laufe des Lebens praktisch nicht.   Knochenaufbau, Sinuslift, Wartezeiten und Knochentransplantate sind heute nicht mehr nötig.</p>
            <p>Daher können wir heute, mit der Technologie der basalen Implantologie, auch bei starkem Knochenrückgang erfolgreich behandeln und den Patienten in wenigen Tagen ein zufriedenes Lächeln geben.</p>
        </div>
