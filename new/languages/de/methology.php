            <h2>5 Vorteile der basalen Implantologie:</h2>
            <ol>
                <li>Alle Patienten, die wünschen, mit Implantaten versorgt zu werden, können ohne Ausnahme behandelt werden. Dies ohne Berücksichtigung des Knochenrückgangs oder der Frage, ob noch Zähne vorhanden sind.</li>
                <li>Es ist nur eine einzige chirurgische Intervention nötig. Basale Implantate werden mit (dauerprovisorischen) Brücken frühzeitig oder sofort belastet und geschient. Die defintitive Brücke wird (sofern die Anfertigung überhaupt nötig ist)  nach 10 - 50 Wochen angefertigt. In vielen Fällen kann auch sofort die definitive Restauration/Brücke angefertigt werden.</li>
                <li>Die geringe Anzahl von Eingriffen und Behandlungsschritten reduziert die Gesamtkosten. Im Komplikationsfall kann schnell und effizient eingegriffen werden,- es wird praktisch immer noch genug Knochen für einen Korrektureingriff vorhanden sein.</li>
                <li>Basale Implantate unterbrechen die Blutversorgung des implantierten Knochens kaum, resp. viel weniger, als vergleichbare herkömmliche Implantate</li>
                <li>Da basale Implantate sofort versorgt werden können, ist es für die Patienten möglich, schnell wieder in ihr normales Leben zurück zu kehren.</li>
