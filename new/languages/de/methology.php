    <div class="columns">
        <div class="col">
            <div class="article">
                <h2>Indikationen für Cortiko-basale Implantate:</h2>
                <ul>
                    <li>Situation in denen einer oder mehrere (oder alle) Zähen fehlen und festsitzender Zahnersatz gewünscht wird.</li>
                    <li>nach dem Verlust von herkömmlichen Zweiphasen-Implantaten, bzw. nach dem Verlust von Knochenaufbauten</li>
                    <li> Behandlungssituation mit stark oder leicht  reduziertem Knochenangebot bis hin zu starker Atrophie</li>
                    <li>Rauchen</li>
                    <li>Diabeties und andere schwere Krankheiten, welche dem Heilungsprozess verlangsamen.</li>
                </ul>
            </div>
        </div>
        <div class="col">
            <div class="article">
                <h2>Kontraindikationen:</h2>
                <ul>
                    <li>i.v. Biphosphonatbehandlung</li>
                    <li>Fälle in denen eine bilaterale Kaufunktion nicht herbeigeführt werden kann, z.B. bei ungleicher Muskelfunktion und bei partiellen Lähmungen vom Muskeln bzw. nach Muskeldissektionen. Diese Fälle können unter Umständen von erfahrenen Spezialisten behandelt werden.</li>
