        <h1>Geschichte</h1>
        <div class="article">
            <p>Die <strong>Dr. IhdeDental</strong> Gruppe wurde 1954  vom Zahntechniker Klaus Ihde in Berlin gegründet. Die Firma zeichnete isch von Anfang an als Lieferant von qualitativ hochwertigen Dentalmaterialen aus. Seit Ende der 80er Jahre wandelte sich das Unternehmen langsam in ein Herstellungsunternehmen um.</p>
            <p>Dr. IhdeDental ist einer der führenden Herstellern von Dentalimplantaten für die Sofortbelastung. Die Produkte werden heute in über 40 Ländern verwendet. Mehr als 100.000 Implantate werden jährlich in den Produktinosstätten in der Schweiz produziert und weltweit eingesetzt.</p>
            <p>Dr. IhdeDental  hat für <strong>Simpladent</strong> implantate und ein Behandlungsverfahren entwickelt, welches selbst in schwierigen Fällenb hervorragende Ergebnisse bringt. Es wird nur sehr wenig Knochen benötigt.  Damit wurden Knochenaufbauten und Knochentransplantationen komplett unnötig.</p>
            <p>Die Unternehmensgruppe wird von Prof. Ihde geführt, der in Deutschland als Zahnarzt ausgebildet wurde und später Weiterbildungen zum Parodontologen und Oralchirurgen absolvierte.  Er entwickelte die morderne basale Implantologie. Unter seiner Führung wurde die Simpladent-Klinikgruppe aufgebaut.</p>
