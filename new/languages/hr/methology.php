            <h2>Što je bazalna implantologija?</h2>
            <p>Termin bazalne implantologije pojavio se prije više od 15 godina.Tada je počeo napredak u modernoj dentalnoj implantologiji. Razvijene su tehnologije za svladavanje najtežih slučajeva ili jake atrofije kada se činilo da ne postoji način da se pomogne pacijentu. Danas I pacijenti sa dijebetesom I ostalim medicinskim problemima mogu biti tretirani uz pomoć bazalnih implantata.</p>
