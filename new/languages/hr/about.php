        <h1>History</h1>
        <div class="article">
            <p><strong>Dr.Ihdedental</strong> je osnovao stomatološki tehničar Klaus Ihde 1954. godine u Berlinu. Od dana osnutka tvrtka se profilirala kao dobavljač visokokvalitetnih dentalnih potrepština. Krajem osamdesetih godina tvrtka se transformira u <strong>Dr.Ihdedental</strong> grupu I započela sa vlastitom proizvodnjom.</p>
            <p>Dr.Ihdedental je jedna od vodećih svjetskih tvrtki za proizvodnju implantata za postupak imedijalnog opterećenja. Danas se proizvodi tvrtke koriste u više od 40 zemalja. Godišnje se proizvede više od 100 000 implantata u našoj tvornici u Švicarskoj.</p>
            <p>Dr.ihdedental je razvio implantate koje je moguće koristiti čak I u neobičnim slučajevima I situacijama gdje nema dovoljno koštane strukture,što je transplantaciju kosti I postupak augumentacije učinilo nepotrebnim.</p>
            <p>Lider tvrtke je Prof.Dr.Stefan Ihde,stomatolog koji je stomatološko obrazovanje stekao u Zapadnoj Njemačkoj. Osim što radi kao peridontolog I oralni kirurg,on je također I osnivač I stručnjak za razvoj moderne bazalne implantologije. Pod njegovim je vodstvom osnovana svjetska mreža dentalnih klinika pod nazivom Simpladent.</p>
