        <h2>Sto Vam možemo ponuditi?</h2>
        <p>Our treatment concept aims at solving all of your problems at the same time, such as:</p>
        <ul>
	    <li>missing teeth, inability to eat and smile; </li>
            <li>periodontal problems (inflammation of the gums, tooth mobility); </li>
            <li>endodontic problems (granulomas, cysts); </li>
            <li>problems with heavily decayed or reconstructed teeth;</li>
            
        </ul>
        <p>Obično pristupamo rješavanju svih problema u jednom koraku. Vađenje zuba I postavljanje implantata se obavljaju u istom terminu I odmah nakon toga naš labaratorijski tim počinje raditi na Vašim novim zubima. Kako bi se izbjegli problemi u budućnosti odstranjuju se I zubi za koje postoji velika vjerojatnost da bi mogli uzrokovati probleme u periodu od narednih 5 godina,te se zamjenjuju implantatima.</p>
        <p>Naše iskustvo stečeno tisućama implantoloških operacija jasno pokazuje da su implantati u odraslih pacijenata mnogo pouzdaniji od njihovih vlastitih zubi.</p>
