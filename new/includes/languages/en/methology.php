        <h1>Implantology</h1>
        <div class="article">
            <h2>What is “Basal Implantology”?</h2>
            <p>The term "basal implantology" appeared more than 15 years ago. At this time a breakthrough in modern dental implantatology started. The technology was developed to cope with the most difficult cases of strong atrophy and when it seemed that there was no other way to help the patient. Today also diabetic patients and patients with other medical issues may receive treatment through basal implants.</p>
            <p>Every implant is fixated at least in two locations in cortical bone. This type of bone provides a high level of mineralization.  The implants are placed exclusively into the dense bone, which everybody has. </p>
            <p>Cortical bone is very capable for load transmission, due to it's high level of mineralization. It gives an opportunity for immediate loading and possibility to place the bridge immediately after implantation. Thus, even in cases of severe bone loss, and complete adentia the patient can get a beautiful smile after within a few days and max. 3 days!!</p>
        </div>
    </div>

    <div class="note">More than 100,000 implants, 10 000 satisfied patients from 42 countries around the world.</div>
    <div class="benefits">
        <div class="article">
            <h2>5 advantages of basal implantology:</h2>
            <ol>
                <li>All patients who request the implants can be treated, without exception, regardless of the extent of bone loss or the presence of absence of residual teeth.</li>
                <li>Only one single intervention is necessary. Basal implants are splinted with (permanent or semi-permanent) bridges very soon after the implant placement. If requested, a final bridge may be places 10-50 weeks later. In many cases the first bridge will be the final one.</li>
                <li>The low number of surgical and other treatment steps reduces overall cost. Rapid and effective measures can be taken should complications occur.</li>
                <li>Basal implants hardly interfere with the blood supply to the host bone, at any rate much less than comparable conventional implants.</li>
                <li>Because basal implants can be immediately loaded, patients can quickly return back to their normal life.</li>
            </ol>
        </div>
