    <h1>Indications and Contraindications</h1>
    <div class="columns">
        <div class="col">
            <div class="article">
                <h2>Indications for basal implants:</h2>
                <ul>
                    <li>all kinds of situations when several teeth are missing or have to be extracted</li>
                    <li>when the procedure of 2-stage implant placement or bone augmentation  has failed</li>
                    <li>all kinds of bone atrophy- from the light cases up to very severe stages</li>
                    <li>Smoking</li>
                    <li>Diabetes and other serious diseases, which slow down the healing process.</li>
                </ul>
            </div>
        </div>
        <div class="col">
            <div class="article">
                <h2>Contraindications:</h2>
                <ul>
                    <li>prolonged oral use of Biphosphonates (the function of osteonal systems must be explored prior to implant therapy)</li>
                    <li>cases where bilateral equal mastication can not be arranged, e.g. when chewing muscles or their innervation are partly missing.</li>
                </ul>
