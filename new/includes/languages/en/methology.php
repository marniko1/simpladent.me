        <h1>Myths & Reality</h1>
        <div class="myths">
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">Dental implants can not be loaded immediately</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">Since more than 20 years it is possible to load dental implants immediately. However with the leading brands of traditional (historical) implants (Straumann, Branemark, Nobel Biocare etc.) this treatment protocol works only under a few, favourable circumstances. With basal implants it almost works always.<br /> With modern basal implants almost all patient cases may be treated in immediate loading protocols.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">Because basal implantology requires less time, the quality of the treatment is reduced.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">Basal implants are more safe to use than conventional (historical) 2-stage implants, because they work in immediate loading. The material quality is anyway the same.<br /> Because they are immediately loaded. Immediate load protocols require no unloaded healing time.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">Implants have to be non-loaded to heal-in well.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">Bone heals only if activated by micro-movements. These movements are directing the process of healing.</div>
                    </div>
                </div>
                            <div class="myth_block">
                    <div class="myth">
                        <div class="header">Myth:</div>
                        <div class="text">Basal implantology is used only in cases with severe atrophy.</div>
                    </div>
                    <div class="reality">
                        <div class="header">Reality:</div>
                        <div class="text">Basal implantology work well in cases of atrophy and in cases without atrophy.</div>
                    </div>
