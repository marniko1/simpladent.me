    <h1>Procedure of the basal implant placement</h1>
    <div class="img">
        <img src="img/metholoy/img6.jpg" alt="" />
    </div>
    <div class="article">
        <p>Inspection of the oral cavity and x-ray examination are done by the treatment provider. After all questions have been answered the treatment date is agreed. The treatment  is often begun immediately because the precuations necessary for conventinal implants (improvement of oral hygiene, periodontal pre-treatment, etc) are not necessary for basal implants, if the involved teeth are simply extracted.</p>
        <p>On the day of operation the teeth are being extracted (if any)  and the modern basal implants, produced  by Dr. Ihde Dental AG in Switzerland, are placed.  Right after the implants are installed, the prosthetical treatment starts: impressions are taken and the bite is registered. After a few hours, maximum during the next day the metal frame try-in or the plastic template (for zirkonium works) will be made. And on the 2nd or 3rd  postoperative day the bridge will be definitively fixed. On this day you can start trying all possibilities of your new teeth - eating, smiling, talking!!!</p>
    </div>
